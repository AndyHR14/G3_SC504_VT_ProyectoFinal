    ID_Producto NUMBER CONSTRAINT Producto_PK PRIMARY KEY,
    Nombre VARCHAR2(100),
    ID_Categoria NUMBER,
    ID_Unidad_Medida NUMBER,
    ID_Estado NUMBER,
    FOREIGN KEY (ID_Categoria) REFERENCES Categoria(ID_Categoria),
